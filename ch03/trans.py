a = input("번역하고 싶은 요일을 적으시오:")

if a == "월":
    print("monday")

elif a == "화":
    print("tuesday")

elif a == "수":
    print("wednesday")

elif a == "목":
    print("thursday")

elif a == "금":
    print("friday")

elif a == "토":
    print("saturday")

elif a == "일":
    print("sunday")

else:
    print("제대로 입력하시오.")

print("종료")